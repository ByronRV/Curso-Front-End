function nowrap(){
    const contenedor = document.querySelector(".flex-wrap")
    contenedor.style.flexWrap = "nowrap";
}function wrap(){
    const contenedor = document.querySelector(".flex-wrap")
    contenedor.style.flexWrap = "wrap";
}function wrapReverse(){
    const contenedor = document.querySelector(".flex-wrap")
    contenedor.style.flexWrap = "wrap-reverse";
}
