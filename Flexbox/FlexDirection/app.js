function row(){
    const contenedor = document.querySelector(".flexbox")
    contenedor.style.flexDirection = "row";
}
function rowReverse(){
    const contenedor = document.querySelector(".flexbox")
    contenedor.style.flexDirection = "row-reverse";
}
